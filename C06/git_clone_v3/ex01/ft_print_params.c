#include <unistd.h>

int	main(int a, char *argv[])
{
	int		i;
	char	*param;
	int		j;

	i = 1;
	j = 0;
	while (i < a)
	{
		param = argv[i];
		j = 0;
		while (param[j] != '\0')
		{
			write(1, &param[j], 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
