#include <unistd.h>

int	main(int a, char *argv[])
{
	char	*param;
	int		j;

	j = 0;
	while (1 < a)
	{
		param = argv[a - 1];
		j = 0;
		while (param[j] != '\0')
		{
			write(1, &param[j], 1);
			j++;
		}
		write(1, "\n", 1);
		a--;
	}
}
