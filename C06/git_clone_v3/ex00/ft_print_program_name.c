#include <unistd.h>
int	main(int a, char *argv[])
{
	char	*name;
	int		i;

	a = 0;
	i = 0;
	name = argv[0];
	while (name[i] != '\0')
	{
		write(1, &name[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
